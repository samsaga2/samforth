INCLUDE kernel.fs
INCLUDE bios.fs

: BOLD ( -- )
    127 3 LSHIFT 7 +
    32 3 LSHIFT
    DO
        I VRAM@
        DUP 1 RSHIFT OR I VRAM!
    LOOP ;

: REDEFINE-A ( -- )
    [CHAR] A 3 LSHIFT
    BIN 00000000 DEC OVER VRAM!
    BIN 00111100 DEC OVER 1 + VRAM!
    BIN 01100110 DEC OVER 2 + VRAM!
    BIN 01100110 DEC OVER 3 + VRAM!
    BIN 01111110 DEC OVER 4 + VRAM!
    BIN 01100110 DEC OVER 5 + VRAM!
    BIN 01100110 DEC OVER 6 + VRAM!
    DROP ;
    
: MESSAGE
    S" AAAAAAAAAAAAA" TYPE CR
    S" SamForth 2009" TYPE CR
    S" by Victor Marzo" TYPE CR
    S" <samsaga2@gmail.com>" TYPE CR CR ;

: MAIN
    32 LINL32 C!
    INIT-MODE32 0 0 15 CHANGE-COLOR
    BOLD
    REDEFINE-A MESSAGE ABORT ;
